def dato(diametro):
    if diametro == 1:
        # varilla de 8mm
        return 0.40

    if diametro == 0:
        # varilla de 12mm
        return 0.60

    if diametro == 2:
        # varilla de 14mm
        return 0.70

    if diametro == 3:
        # varilla de 16mm
        return 0.80

    if diametro == 4:
        # varilla de 18mm
        return 0.90

    if diametro == 5:
        # varilla de 20mm
        return 1
